# **if-else Statements**
num=int(input("Enter a number: "))
if num%2==0:
  print("The number is even")
else:
  print("The number is odd")


# **IF-ELIF-ELSE STATEMENT**
Marks=int(input('Enter your marks:'))
if Marks>=90:
  print('Grade:A')
elif Marks>=75:
  print('Grade B:')
elif Marks>=50:
  print('Grade C:')
else:
  print('Grade F:')
